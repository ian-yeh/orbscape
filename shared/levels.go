func Parselevel(rows []string) [][]TileType {
	grid := make([][]TileType, len(rows))
	for y, row := range rows {
		grid[y] = make([]TileType, len(row))
		for x, ch := range row {
			switch ch {
			case '#':
				grid[y][x] = Wall
			case 'G':
				grid[y][x] = Goal
			default: 
				grid[y][x] = Empty
			}
		}
	}

	return grid
}
